export async function getPricingMap(env) {
  try {
    if (env.SJ_DATA) {
      const custom = await env.SJ_DATA.get('pricing:map');
      if (custom && custom.trim()) return custom;
    }
  } catch { /* fall back to default */ }
  return DEFAULT_PRICING_MAP;
}
